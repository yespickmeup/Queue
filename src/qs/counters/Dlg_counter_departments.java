    private void ok() {
        Field.Combo cl = (Field.Combo) jTextField3;
        String department_id = cl.getId();
        String department = cl.getText();
        int priority = 0;
        if (jCheckBox1.isSelected()) {
            priority = 1;
        }
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(department_id, department, priority));
        }
    }
