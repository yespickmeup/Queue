    List<Counters.to_counters> counters = new ArrayList();

    private void init_counter() {

        Object[][] obj = new Object[counters.size()][2];
        int i = 0;
        for (Counters.to_counters to : counters) {
            obj[i][0] = " " + to.counter;
            obj[i][1] = " " + to.department;
            i++;
        }
        JLabel[] labels = {};
        int[] tbl_widths_customers = {100,100};
        String[] col_names = {"",""};
        TableRenderer tr = new TableRenderer();
        TableRenderer.setPopup(jTextField2, obj, labels, tbl_widths_customers, col_names);
        tr.setCallback(new TableRenderer.Callback() {
            @Override
            public void ok(TableRenderer.OutputData data) {
                Counters.to_counters co = (Counters.to_counters) counters.get(data.selected_row);
                Field.Combo cl = (Field.Combo) jTextField2;
                cl.setText(co.counter);
                cl.setId("" + co.id);
            }
        });
    }

    private void ok() {
        Field.Combo co = (Field.Combo) jTextField2;

        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData("" + co.getId(), co.getText()));
        }
    }
