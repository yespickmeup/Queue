    private void start_server() {
        int port = FitIn.toInt(jTextField1.getText());
        if (port < 1) {
            Alert.set(0, "Input Port!");
            jTextField1.grabFocus();
            return;
        }
        try {

            Thread t = new Thread(new Runnable() {
                @Override
                public void run() {
                    ChatServer.set(port);
                }
            });
            t.start();

        } catch (Exception e) {
        }
    }
